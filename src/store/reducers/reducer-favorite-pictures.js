const reducerFavorite = (state = initialState, action) => {
	switch (action.type) {
		case 'ADD_FAVORITE': {
			return { state, pictures: [...state.pictures, action.payload] };
		}
		case 'REMOVE_FAVORITE': {
			return { state, pictures: state.pictures.filter((item) => item.id !== action.payload.id) };
		}
		default:
			return state;
	}
};

export default reducerFavorite;
